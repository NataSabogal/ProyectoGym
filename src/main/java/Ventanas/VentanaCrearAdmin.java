    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
         String nombre = txtNombre.getText();
        String cedula = txtCedula.getText();
        String password = new String(txtContra.getText());

        AdminService adminService = new AdminService();

        if (adminService.registerAdmin(cedula, nombre, password)) {
            JOptionPane.showMessageDialog(null, "Administrador registrado con éxito");
        } else {
            JOptionPane.showMessageDialog(null, "Error al registrar. Verifica los datos.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnCrearActionPerformed

